	<h1><font color="white">Warren Buffet</font></h1><hr>
	<p>
	Warren Edward Buffett (/ˈbʌfᵻt/; born August 30, 1930)[3] is an American business magnate, investor and philanthropist. He is the most successful investor in the world.[4][5][6] Buffett is the chairman, CEO and largest shareholder of Berkshire Hathaway,[7] and is consistently ranked among the world's wealthiest people. He was ranked as the world's wealthiest person in 2008[8] and as the third wealthiest in 2015.[9] In 2012 Time named Buffett one of the world's most influential people.</p>
<p>
Buffett is often referred to as the "Wizard of Omaha" or "Oracle of Omaha,"[11] or the "Sage of Omaha,"[12] and is noted for his adherence to value investing and for his personal frugality despite his immense wealth.[13] Buffett is a notable philanthropist, having pledged to give away 99 percent[14] of his fortune to philanthropic causes, primarily via the Gates Foundation. On April 11, 2012, he was diagnosed with prostate cancer,[15] for which he successfully completed treatment in September 2012.[16] Buffett is also active in contributing to political causes, having endorsed Democratic candidate Hillary Clinton for president during the 2016 campaign season.[17]
</p>
