<div class="early">
	<p>
<b><h2>Early life</b><hr></h2>
Buffett was born in 1930 in Omaha, Nebraska, of distant French Huguenot descent.[18] He was the second of three children and the only son of Leila (née Stahl) and Congressman Howard Buffett,[19] Buffett began his education at Rose Hill Elementary School. In 1942, his father was elected to the first of four terms in the United States Congress, and after moving with his family to Washington, D.C., Warren finished elementary school, attended Alice Deal Junior High School and graduated from Woodrow Wilson High School in 1947, where his senior yearbook picture reads: "likes math; a future stockbroker."[20] After finishing high school and finding success with his side entrepreneurial and investment ventures, Buffett wanted to skip college to go directly into business but was overruled by his father.[21][22]</p>

<p>Buffett displayed an interest in business and investing at a young age. Much of Buffett's early childhood years were enlivened with entrepreneurial ventures. One of his first business ventures, Buffett sold chewing gum, Coca-Cola bottles, or weekly magazines door to door. He worked in his grandfather's grocery store. While still in high school, he made money delivering newspapers, selling golf balls and stamps, and detailing cars, among other means. On his first income tax return in 1944, Buffett took a $35 deduction for the use of his bicycle and watch on his paper route.[23] In 1945, as a high school sophomore, Buffett and a friend spent $25 to purchase a used pinball machine, which they placed in the local barber shop. Within months, they owned several machines in three different barber shops across Omaha. The business was sold later in the year for $1,200 to a war veteran.</p>

<p>Buffett's interest in the stock market and investing dated to schoolboy days he spent in the customers' lounge of a regional stock brokerage near his father's own brokerage office. On a trip to New York City at age ten, he made a point to visit the New York Stock Exchange. At 11, he bought three shares of Cities Service Preferred for himself, and three for his sister Doris Buffett (founder of The Sunshine Lady Foundation[25]).[26][27] At the age of 15, Warren made more than $175 monthly delivering Washington Post newspapers. In high school, he invested in a business owned by his father and bought a 40-acre farm worked by a tenant farmer. He bought the land when he was 14 years old with $1,200 of his savings. By the time he finished college, Buffett had accumulated more than $90,000 in savings measured in 2009 dollars.</p>

<p>In 1947, Buffett entered the Wharton School of the University of Pennsylvania. He would have preferred to focus on his business ventures; however, he enrolled due to pressure from his father.[24] Warren studied there for two years and joined the Alpha Sigma Phi fraternity.[29] He then transferred to the University of Nebraska–Lincoln where at 19, he graduated with a Bachelor of Science in Business Administration. After being rejected by Harvard Business School, Buffett enrolled at Columbia Business School upon learning that Benjamin Graham taught there. He earned a Master of Science in Economics from Columbia in 1951. Buffett also attended the New York Institute of Finance.</p>

<p>The basic ideas of investing are to look at stocks as business, use the market's fluctuations to your advantage, and seek a margin of safety. That's what Ben Graham taught us. A hundred years from now they will still be the cornerstones of investing.<br><br>

— Warren Buffett</p></div>



<div class="business">
<b><h2>Business career</b><hr></h2>
<p>Buffett worked from 1951 to 1954 at Buffett-Falk & Co. as an investment salesman; from 1954 to 1956 at Graham-Newman Corp. as a securities analyst; from 1956 to 1969 at Buffett Partnership, Ltd. as a general partner and from 1970, as Chairman and CEO of Berkshire Hathaway Inc.</p>

<p>By 1950, at 20, Buffett had made and saved $9,800 (over $96,000 inflation adjusted for the 2014 USD[33]).[34] In April 1952, Buffett discovered that Graham was on the board of GEICO insurance. Taking a train to Washington, D.C. on a Saturday, he knocked on the door of GEICO's headquarters until a janitor admitted him. There he met Lorimer Davidson, Geico's Vice President, and the two discussed the insurance business for hours. Davidson would eventually become Buffett's lifelong friend and a lasting influence,[35] and would later recall that he found Buffett to be an "extraordinary man" after only fifteen minutes. Buffett wanted to work on Wall Street; however, both his father and Ben Graham urged him not to. He offered to work for Graham for free, but Graham refused.</p>

<p>Buffett returned to Omaha and worked as a stockbroker while taking a Dale Carnegie public speaking course.[37] Using what he learned, he felt confident enough to teach an "Investment Principles" night class at University of Nebraska-Omaha. The average age of his students was more than twice his own. During this time he also purchased a Sinclair Texaco gas station as a side investment. However, this was not successful.[citation needed]</p>

<p>In 1952,[38] Buffett married Susan Thompson at Dundee Presbyterian Church. The next year they had their first child, Susan Alice. In 1954, Buffett accepted a job at Benjamin Graham's partnership. His starting salary was $12,000 a year (approximately $105,000 inflation adjusted for the 2012 USD[33]). There he worked closely with Walter Schloss. Graham was a tough boss. He was adamant that stocks provide a wide margin of safety after weighting the trade-off between their price and their intrinsic value. The argument made sense to Buffett but he questioned whether the criteria were too stringent and caused the company to miss out on big winners that had other appealing features.[citation needed] That same year the Buffetts had their second child, Howard Graham. In 1956, Benjamin Graham retired and closed his partnership. At this time Buffett's personal savings were over $174,000 ($1.47 million 2012 USD[33]) and he started Buffett Partnership Ltd..</p>


<p>Buffett's home in Omaha
In 1957, Buffett operated three partnerships. He purchased a five-bedroom stucco house in Omaha, where he still lives, for $31,500.[citation needed] In 1958 the Buffetts' third child, Peter Andrew, was born. Buffett operated five partnerships that year. In 1959, the company grew to six partnerships and Buffett met future partner Charlie Munger. By 1960, Buffett operated seven partnerships.[citation needed] He asked one of his partners, a doctor, to find ten other doctors willing to invest $10,000 each in his partnership.[citation needed] Eventually eleven agreed, and Buffett pooled their money with a mere $100 original investment of his own.[citation needed] In 1961, Buffett revealed that Sanborn Map Company accounted for 35% of the partnership's assets.[citation needed] He explained that in 1958 Sanborn stock sold at only $45 per share when the value of the Sanborn investment portfolio was $65 per share.[citation needed] This meant that buyers valued Sanborn stock at "minus $20" per share and were unwilling to pay more than 70 cents on the dollar for an investment portfolio with a map business thrown in for nothing which earned him a spot on Sanborn's board.</p>
</div>

<div class="millionaire">
<p>
<b><h2>As a millionaire</b></h2><hr>
In 1962, Buffett became a millionaire because of his partnerships, which in January 1962 had an excess of $7,178,500, of which over $1,025,000 belonged to Buffett. He merged these partnerships into one. Buffett invested in and eventually took control of a textile manufacturing firm, Berkshire Hathaway. He began buying shares in Berkshire from Seabury Stanton, the owner, whom he later fired. Buffett's partnerships began purchasing shares at $7.60 per share. In 1965, when Buffett's partnerships began purchasing Berkshire aggressively, they paid $14.86 per share while the company had working capital of $19 per share. This did not include the value of fixed assets (factory and equipment). Buffett took control of Berkshire Hathaway at a board meeting and named a new president, Ken Chace, to run the company. In 1966, Buffett closed the partnership to new money. He later claimed that the textile business had been his worst trade.[39] He then moved the business into the insurance sector, and, in 1985, the last of the mills that had been the core business of Berkshire Hathaway was sold. Buffett wrote in his letter: "... unless it appears that circumstances have changed (under some conditions added capital would improve results) or unless new partners can bring some asset to the partnership other than simply capital, I intend to admit no additional partners to BPL."[citation needed]</p>

<p>In a second letter, Buffett announced his first investment in a private business — Hochschild, Kohn and Co, a privately owned Baltimore department store. In 1967, Berkshire paid out its first and only dividend of 10 cents. In 1969, following his most successful year, Buffett liquidated the partnership and transferred their assets to his partners. Among the assets paid out were shares of Berkshire Hathaway. In 1970, Buffett began writing his now-famous annual letters to shareholders. However, he lived solely on his salary of $50,000 per year and his outside investment income. In 1979, Berkshire began the year trading at $775 per share, and ended at $1,310. Buffett's net worth reached $620 million.[citation needed]</p>

<p>In 1973, Berkshire began to acquire stock in the Washington Post Company. Buffett became close friends with Katharine Graham, who controlled the company and its flagship newspaper, and joined its board. In 1974, the SEC opened a formal investigation into Buffett and Berkshire's acquisition of WESCO, due to possible conflict of interest. No charges were brought. In 1977, Berkshire indirectly purchased the Buffalo Evening News for $32.5 million. Antitrust charges started, instigated by its rival, the Buffalo Courier-Express. Both papers lost money, until the Courier-Express folded in 1982.[citation needed]</p>

<p>In 1979, Berkshire began to acquire stock in ABC. Capital Cities announced a $3.5 billion purchase of ABC on March 18, 1985 surprising the media industry, as ABC was four times bigger than Capital Cities at the time. Buffett helped finance the deal in return for a 25% stake in the combined company.[40] The newly merged company, known as Capital Cities/ABC (or CapCities/ABC), was forced to sell some stations due to U.S. Federal Communications Commission ownership rules. The two companies also owned several radio stations in the same markets.[41]</p>

<p>In 1987, Berkshire Hathaway purchased a 12% stake in Salomon Inc., making it the largest shareholder and Buffett a director. In 1990, a scandal involving John Gutfreund (former CEO of Salomon Brothers) surfaced. A rogue trader, Paul Mozer, was submitting bids in excess of what was allowed by Treasury rules. When this was brought to Gutfreund's attention, he did not immediately suspend the rogue trader. Gutfreund left the company in August 1991.[42] Buffett became Chairman of Salomon until the crisis passed.[43]</p>

<p>In 1988, Buffett began buying The Coca-Cola Company stock, eventually purchasing up to 7% of the company for $1.02 billion. It would turn out to be one of Berkshire's most lucrative investments, and one which it still holds.[citation needed]</p>

<b><h2>As a billionaire</h2></b><hr>
<p>Buffett became a paper billionaire when Berkshire Hathaway began selling class A shares on May 29, 1990, with the market closing at US$7,175 a share.[44] In 1998 he acquired General Re (Gen Re) as a subsidiary in a deal that presented difficulties—according to the Rational Walk investment website, "underwriting standards proved to be inadequate," while a "problematic derivatives book" was resolved after numerous years and a significant loss.[45] Gen Re later provided reinsurance after Buffett became involved with Maurice R. Greenberg at AIG in 2002.[46]</p>

<p>During a 2005 investigation of an accounting fraud case involving AIG, Gen Re executives became implicated. On March 15, 2005, the AIG board forced Greenberg to resign from his post as Chairman and CEO after New York state regulators claimed that AIG had engaged in questionable transactions and improper accounting.[47] On February 9, 2006, AIG agreed to pay a US$1.6 billion fine.[48] In 2010 the U.S. government agreed to a US$92 million settlement with Gen Re, allowing the Berkshire Hathaway subsidiary to avoid prosecution in the AIG case. Gen Re also made a commitment to implement "corporate governance concessions," which required Berkshire Hathaway’s Chief Financial Officer to attend General Re’s audit committee meetings and mandated the appointment of an independent director.[45]</p>

<p>In 2002, Buffett entered in US$11 billion worth of forward contracts to deliver U.S. dollars against other currencies. By April 2006, his total gain on these contracts was over US$2 billion. In 2006, Buffett announced in June that he gradually would give away 85% of his Berkshire holdings to five foundations in annual gifts of stock, starting in July 2006—the largest contribution would go to the Bill and Melinda Gates Foundation.[49]</p>

<p>In 2007, in a letter to shareholders, Buffett announced that he was looking for a younger successor, or perhaps successors, to run his investment business.[50] Buffett had previously selected Lou Simpson, who runs investments at Geico, to fill the role; however, Simpson is only six years younger than Buffett.[citation needed]</p>

<p>On August 14, 2014, the price of Berkshire Hathaway's shares hit US$200,000 a share for the first time, capitalizing the company at US$328 billion. While Buffett had given away much of his stock to charities by this time, he still held 321,000 shares worth US$64.2 billion. On August 20, 2014, Berkshire Hathaway was fined $896,000 for failing to report December 9, 2013 purchase of shares in USG Corporation as required.[51]</p>

<b><h2>Great Recession</h2></b><hr>
<p>Buffett ran into criticism[52] during the subprime crisis of 2007–2008, part of the recession that started in 2007, that he had allocated capital too early resulting in suboptimal deals. "Buy American. I am." he wrote for an opinion piece published in the New York Times in 2008.[53] Buffett called the downturn in the financial sector that started in 2007 "poetic justice".[54] Buffett's Berkshire Hathaway suffered a 77% drop in earnings during Q3 2008 and several of his later deals suffered large mark-to-market losses.[55]</p>

<p>Berkshire Hathaway acquired 10% perpetual preferred stock of Goldman Sachs.[56] Some of Buffett's put options (European exercise at expiry only) that he wrote (sold) were running at around $6.73 billion mark-to-market losses as of late 2008.[57] The scale of the potential loss prompted the SEC to demand that Berkshire produce, "a more robust disclosure" of factors used to value the contracts.[57] Buffett also helped Dow Chemical pay for its $18.8 billion takeover of Rohm & Haas. He thus became the single largest shareholder in the enlarged group with his Berkshire Hathaway, which provided $3 billion, underlining his instrumental role during the crisis in debt and equity markets.[58]</p>

<p>In 2008, Buffett became the richest person in the world, with a total net worth estimated at $62 billion[59] by Forbes and at $58 billion[60] by Yahoo, overtaking Bill Gates, who had been number one on the Forbes list for 13 consecutive years.[61] In 2009, Gates regained the top position on the Forbes list, with Buffett shifted to second place. Both of the men's values dropped, to $40 billion and $37 billion respectively—according to Forbes, Buffett lost $25 billion over a 12-month period during 2008/2009.[62]</p>

<p>In October 2008, the media reported that Buffett had agreed to buy General Electric (GE) preferred stock.[63] The operation included special incentives: He received an option to buy three billion shares of GE stock, at $22.25, over the five years following the agreement, and Buffett also received a 10% dividend (callable within three years). In February 2009, Buffett sold some Procter & Gamble Co. and Johnson & Johnson shares from his personal portfolio.[64]</p>
<p>In addition to suggestions of mistiming, the wisdom in keeping some of Berkshire's major holdings, including The Coca-Cola Company, which in 1998 peaked at $86, raised questions. Buffett discussed the difficulties of knowing when to sell in the company's 2004 annual report:</p>

<p>That may seem easy to do when one looks through an always-clean, rear-view mirror. Unfortunately, however, it's the windshield through which investors must peer, and that glass is invariably fogged.[65]</p>

<p>In March 2009, Buffett said in a cable television interview that the economy had "fallen off a cliff ... Not only has the economy slowed down a lot, but people have really changed their habits like I haven't seen". Additionally, Buffett feared that inflation levels that occurred in the 1970s—which led to years of painful stagflation—might re-emerge.</p>

<p>In 2009, Buffett invested $2.6 billion as a part of Swiss Re's campaign to raise equity capital.[68][69] Berkshire Hathaway already owned a 3% stake, with rights to own more than 20%.[70] Also in 2009, Buffett acquired Burlington Northern Santa Fe Corp. for $34 billion in cash and stock. Alice Schroeder, author of Snowball, said that a key reason for the purchase was to diversify Berkshire Hathaway from the financial industry.[71] Measured by market capitalization in the Financial Times Global 500, Berkshire Hathaway was the eighteenth largest corporation in the world as of June 2009.[72]</p>

<p>In 2009, Buffett divested his failed investment in ConocoPhillips, saying to his Berkshire investors,</p>

<p>I bought a large amount of ConocoPhillips stock when oil and gas prices were near their peak. I in no way anticipated the dramatic fall in energy prices that occurred in the last half of the year. I still believe the odds are good that oil sells far higher in the future than the current $40–$50 price. But so far I have been dead wrong. Even if prices should rise, moreover, the terrible timing of my purchase has cost Berkshire several billion dollars.[73]</p>

<p>The merger with the Burlington Northern Santa Fe Railway (BNSF) closed upon BNSF shareholder approval in 1Q2010. This deal was valued at approximately $34 billion and represented an increase of the previously existing stake of 22%.[citation needed]</p>

<p>In June 2010, Buffett defended the credit-rating agencies for their role in the US financial crisis, claiming:</p>

<p>Very, very few people could appreciate the bubble. That's the nature of bubbles – they're mass delusions.[74]</p>

<p>On March 18, 2011, Goldman Sachs was given Federal Reserve approval to buy back Berkshire's preferred stock in Goldman. Buffett had been reluctant to give up the stock, which averaged $1.4 million in dividends per day,[75][76] saying:</p>

<p>I'm going to be the Osama bin Laden of capitalism. I'm on my way to an unknown destination in Asia where I'm going to look for a cave. If the U.S. Armed forces can't find Osama bin Laden in 10 years, let Goldman Sachs try to find me.[77]</p>

<p>In November 2011, it was announced that over the course of the previous eight months, Buffett had bought 64 million shares of International Business Machine Corp (IBM) stock, worth around $11 billion. This unanticipated investment raised his stake in the company to around 5.5 percent—the largest stake in IBM alongside that of State Street Global Advisors. Buffett had said on numerous prior occasions that he would not invest in technology because he did not fully understand it, so the move came as a surprise to many investors and observers. During the interview, in which he revealed the investment to the public, Buffett stated that he was impressed by the company's ability to retain corporate clients and said, "I don't know of any large company that really has been as specific on what they intend to do and how they intend to do it as IBM."[78]</p>

<p>In May 2012, Buffett's acquisition of Media General, consisting of 63 newspapers in the south-eastern U.S., was announced.[79] The company was the second news print purchase made by Buffett in one year.[80]</p>

<p>Interim publisher James W. Hopson announced on July 18, 2013 that the Press of Atlantic City would be sold to Buffett’s BH Media Group by ABARTA, a private holding company based in Pittsburgh, U.S. At the Berkshire shareholders meeting in May 2013, Buffett explained that he did not expect to "move the needle" at Berkshire with newspaper acquisitions, but he anticipates an annual return of 10 percent. The Press of Atlantic City became Berkshire's 30th daily newspaper, following other purchases such as Virginia, U.S.' Roanoke Times and The Tulsa World in Oklahoma, U.S.[81]</p>

<p>During a presentation to Georgetown University students in Washington, D.C. in late September 2013, Buffett compared the U.S. Federal Reserve to a hedge fund and stated that the bank is generating "$80 billion or $90 billion a year probably" in revenue for the U.S. government. Buffett also advocated further on the issue of wealth equality in society:</p>

<p>We have learned to turn out lots of goods and services, but we haven’t learned as well how to have everybody share in the bounty. The obligation of a society as prosperous as ours is to figure out how nobody gets left too far behind.</p>

<p>After the difficulties of the economic crisis, Buffett managed to bring its company back to its pre-recession standards: in Q2 2014, Berkshire Hathaway made $6.4 billion in net profit, the most it had ever made in a three-month period.</p>

